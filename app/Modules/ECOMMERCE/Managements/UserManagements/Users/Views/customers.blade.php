        $('body').on('click', '.deleteBtn', function() {
            var id = $(this).data("id");
            if (confirm("Are You sure want to delete !")) {
                $.ajax({
                    type: "GET",
                    url: "{{ url('delete/customer') }}" + '/' + id,
                    success: function(data) {

                        if (data.data == 1) {
                            table.draw(false);
                            toastr.error("Customer has been Deleted", "Deleted Successfully");
